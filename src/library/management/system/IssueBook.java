    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(txtbookid.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Please Enter the bookID and search");
            txtbookid.requestFocus();
        }
        else{
            try {
                PreparedStatement pst = con.prepareStatement("update book set status=?,issuedate=?,duedate=?,studentid=?");
                pst.setString(1, "ISSUED");
                pst.setString(2, txtdate.getText());
                pst.setString(3, txtduedate.getText());
                pst.setString(4, txtstudid.getText());
                
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Book Issued Successfully");
                txtbookid.setText("");
                txtname.setText("");
                txtstudid.setText("");
                txtdate.setText("");
                txtduedate.setText("");
            } catch (SQLException ex) {
                Logger.getLogger(IssueBook.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

