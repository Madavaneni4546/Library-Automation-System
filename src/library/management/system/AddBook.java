    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        if(txtid.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All fields are Required");
            txtid.requestFocus();
        }
        else if(txtname.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All fields are Required");
            txtname.requestFocus();
        }
        else if(txtpublisher.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All fields are Required");
            txtpublisher.requestFocus();
        }
        else if(txtprice.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All fields are Required");
            txtprice.requestFocus();
        }
        else if(txtpublishername.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All fields are Required");
            txtpublishername.requestFocus();
        }
        else{
            Connection con =Connect.Connection();
            try {
                PreparedStatement pst = con.prepareStatement("INSERT INTO BOOK(id,name,publisher,price,year,status)values(?,?,?,?,?,?)");
                pst.setString(1, txtid.getText());
                pst.setString(2, txtname.getText());
                pst.setString(4, txtprice.getText());
                pst.setString(3, txtpublisher.getText());
                pst.setString(5, txtpublishername.getText());
                pst.setString(6, "NOT ISSUED");
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Saved Successfully");
                txtid.setText("");
                txtname.setText("");
                txtprice.setText("");
                txtpublisher.setText("");
                txtpublishername.setText("");
            } catch (SQLException ex) {
                Logger.getLogger(StudentRegistration.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
            
    }//GEN-LAST:event_btnsaveActionPerformed

