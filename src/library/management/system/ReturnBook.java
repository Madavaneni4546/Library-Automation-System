    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(txtstudid.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Please Enter StudentID and search");
            txtstudid.requestFocus();
        }
        else{
            try {
                pst=con.prepareStatement("select s.name,b.id,b.name,b.issuedate,b.duedate from book b inner join student s using(id) where b.id=?");
                pst.setString(1,txtstudid.getText());
                rs=pst.executeQuery();
                if(rs.next()){
                    txtbookid.setText(rs.getString(2));
                    txtstudname.setText(rs.getString(1));
                    txtduedate.setText(rs.getString(5));
                    txtbookname.setText(rs.getString(3));
                    txtdate.setText(rs.getString(4));
                }
                else{
                    JOptionPane.showMessageDialog(this, "Please Enter valid Student ID");
                }
            } catch (SQLException ex) {
                Logger.getLogger(ReturnBook.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(txtstudid.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Please Enter StudentID and search");
            txtstudid.requestFocus();
        }
        else{
            try {
                pst=con.prepareStatement("UPDATE BOOK SET STATUS=?,ISSUEDATE=?,DUEDATE=? , STUDENTID=? WHERE BOOK.ID=?");
                pst.setString(1,"ISSUED");
                pst.setString(2, "");
                pst.setString(3, "");
                pst.setString(4, " ");
                pst.setString(5, txtbookid.getText());
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this,"Book Returned");
                
                txtdate.setText("");
                txtbookid.setText("");
                txtbookname.setText("");
                txtstudid.setText("");
                txtstudname.setText("");
                txtduedate.setText("");     
                
            } catch (SQLException ex) {
                Logger.getLogger(ReturnBook.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

